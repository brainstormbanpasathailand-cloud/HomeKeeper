const dict: Record<Lang, Record<string, string>> = { th, zh, en, ru }

interface I18n {
  lang: Lang
  setLang: (l: Lang) => void
  t: (key: string) => string
}

const I18nContext = createContext<I18n>({ lang: 'th', setLang: () => {}, t: (k) => k })

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>((localStorage.getItem('hk_lang') as Lang) || 'th')
  const value = useMemo<I18n>(
    () => ({
      lang,
      setLang: (l) => {
        localStorage.setItem('hk_lang', l)
        setLangState(l)
      },
      t: (key) => dict[lang][key] ?? dict.en[key] ?? key,
    }),
    [lang],
  )
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export const useI18n = () => useContext(I18nContext)
